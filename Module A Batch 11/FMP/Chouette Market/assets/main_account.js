const orderBtn = document.querySelector(".orderBtn");
const orderHistory = document.querySelector(".orderHistory");
const accountDetails = document.querySelector(".accountDetails");
const detailsBtn = document.querySelector(".detailsBtn");
const customers = document.querySelectorAll(".customer");
const allBtns = document.querySelectorAll(".accountMenu li button");

allBtns.forEach((btn, ind) => {
    btn.addEventListener("click", () => {
        customers.forEach((c) => {
            c.classList.add("sectionHidden")
        });
        document.querySelector(`.${btn.dataset.label}`).classList.remove('sectionHidden');
        allBtns.forEach((b) => {
          b.classList.remove("isActive");
        })
        btn.classList.add("isActive");
    });
})
